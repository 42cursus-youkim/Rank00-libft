int	isalpha(int c)
{
	unsigned char chr;

	if (!(0 <= c && c <= 255))
		return (0);
	chr = (unsigned char)c;
	if (('A' <= chr && chr <= 'Z') || ('a' <= chr && chr <= 'z'))
		return (1);
	return (0);
}
