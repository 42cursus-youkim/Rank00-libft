int	isprint(int c)
{
	unsigned char chr;

	if (!(0 <= c && c <= 255))
		return (0);
	chr = (unsigned char)c;
	if((' ' <= chr && chr <= '\'') || ('(' <= chr && chr <= '/')
		|| ('0' <= chr && chr <= '9') || ( ':' <= chr && chr <= '?') || chr == '@'
		|| ('A' <= chr && chr <= 'Z') || ('a' <= chr && chr <= 'z')
		|| ('[' <= chr && chr <= '_') || ('{' <= chr && chr <= '~') || chr == '`')
		return (1);
	return (0);
}
